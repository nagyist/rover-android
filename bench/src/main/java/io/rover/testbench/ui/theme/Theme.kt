package io.rover.testbench.ui.theme

import androidx.compose.foundation.isSystemInDarkTheme
import androidx.compose.material3.MaterialTheme
import androidx.compose.material3.darkColorScheme
import androidx.compose.material3.lightColorScheme
import androidx.compose.runtime.Composable

internal val DarkColorPalette = darkColorScheme(
    primary = Cyan200,
    secondary = Blue200
)

internal val LightColorPalette = lightColorScheme(
    primary = Cyan500,
    secondary = Blue200
)

@Composable
fun RoverAndroidTheme(darkTheme: Boolean = isSystemInDarkTheme(), content: @Composable () -> Unit) {
    val colorScheme = if (darkTheme) {
        DarkColorPalette
    } else {
        LightColorPalette
    }

    MaterialTheme(
        colorScheme = colorScheme,
        content = content
    )
}
